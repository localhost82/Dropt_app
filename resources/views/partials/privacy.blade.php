        <li>What data the website collects from the user</li>
        <li>When the organisation or entity collects personal information</li>
        <li>How the organisation or entity uses personal information</li>
        <li>If the website uses cookies</li>
        <li>Who else has access to the data collected (such as third party advertisers or business partners)</li>
        <li>How the company or organisation will protect the user’s data from misuse and uphold their legal responsibilities regarding data use</li>
        <li>If the company can sell the user’s data to other enterprises</li>
        <li>How the user can hold the organisation or entity responsible for the misuse of their data</li>
        <li>How the user can opt-out of data sharing and what impact that might have on their experience</li>
